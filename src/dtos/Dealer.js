export class Dealer {

	constructor(dealerName, dealerCode, dealerPhone, dealerEmail, street, city, state, zip) {
		this.name = dealerName;
		this.code = dealerCode;
		this.phone = dealerPhone;
		this.email = dealerEmail;
		this.street = street;
		this.city = city;
		this.state = state;
		this.zip = zip;
	}
}